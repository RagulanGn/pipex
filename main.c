#include "pipex.h"

int	main(int argc, char *argv[], char *envp[])
{
	t_pipex	*pipex;

	if (argc < 5)
		return (0);
	pipex = init_pipex(argc, argv);
	if (parsing(pipex, argc, argv, envp) == NULL)
		return (0);
	execute(pipex, argv, envp, argc);
}
